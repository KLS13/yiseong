public class ForBasic {

	public static void main(String[] args) {
	
		/*
		 for(초기화 ; 조건삭; 증감식){
		 
		 }
		 */
		
		for(int i = 0 ; i<3 ; i++) {
			
			System.out.println("현재 i값" + i);
			 
		}		
		
		int sum = 0;
		for(int i = 1 ; i<=10 ; i++) {
			sum+=i;
		
		}
		System.out.println(sum);
	}
	
}


