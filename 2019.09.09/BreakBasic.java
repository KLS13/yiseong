public class BreakBasic {

	public static void main(String[] args) {

		int num = 0 ;
		while(num<10) { // 10보다 작을때까지
			num++; // num 에 1씩 더해서 저장한다.
			if(num==3) { // 만약 num이 3이라면
				
				System.out.println("넘값"+num);
				break;  // num값을 출력하고 break;
			}
			
			
			
			
		}
	}

}
